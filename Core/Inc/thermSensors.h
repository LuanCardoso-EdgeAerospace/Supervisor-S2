#define TMP468_DEFAULT_THERM_LIMIT (0x1FF)
/*
basename, addr, i2cbus, conversion_rate, hysteresis, THERM1, THERM2, expectedChannelsMask,
basename: base string
conversion_rate: one of TMP468_CONVERSION_RATE_*; defaults to TMP468_CONVERSION_RATE_CONTINUOUS
hysteresis: Hysteresis in degress C.
THERM1, THERM2: Therinmal alarm, LSB = 0.5 Degrees. Replicates to all remotes. 0x1FF disables.
i2cbus: identification for the i2c bus
expectedChannelsMask: bitmask with the expected enabled channels. Used during init for error reporting
*/
